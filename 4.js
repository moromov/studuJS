//Операторы сравнения и условия

/*

Операторы сравнения
>, <, >=,<=

== - нестрогое равно
=== - строгое равно
!= - нестрогое неравенство
!== - строгое равенство

*/

console.log(10>5); //true
console.log(20>90); //false

let result = 10>5;
console.log(result); //true

console.log('5' == 5); //true
console.log('5' === 5); //false

