// Типы переменных

//let - переменная
//const - константа

//let - может менять значение во время работы программы
//const - не могут менять значение

const PI = 3.14;
console.log(PI);

//var - старый формат объявление переменной
//В отличии от let и const имеет функциональную область видимости, а не блочную
